get_header();
?>

	<div id="primary" class="content-area">
		<main id="main" class="site-main">

		<?php
		while ( have_posts() ) :
			the_post();

			?>
        <div class="">
            <!-- Support Head -->
            <?php echo do_shortcode('[breadcrumbs]');?>
            <!-- Support Head -->
            <section class="content-page-default"><?php the_content();?></section>
        </div>
        <?php
			if ( comments_open() || get_comments_number() ) :
				comments_template();
			endif;

		endwhile; // End of the loop.
		?>

